def _queue_x_posts(
    post_urn: str,
    x_posts: list[str],
    x_thread: list[str],
    auto_post_x: bool,
    transform_output: dict,
) -> dict:
    """
    Queue X posts and optionally auto-post if enabled.

    Returns:
        {"queued_single": list[str], "queued_thread": list[str], "posted": list[dict]}
    """
    queued_single: list[str] = []
    queued_thread: list[str] = []
    posted: list[dict] = []

    # Individual X posts
    for i, tweet_text in enumerate(x_posts):
        item_id = add_to_queue(
            post_urn=post_urn,
            platform="x_post",
            content=tweet_text,
            requires_review=False,
            metadata={"tweet_index": i, "pillar": transform_output.get("CONTENT_CLASSIFICATION", {})},
        )
        queued_single.append(item_id)

        if auto_post_x and is_auto_post_enabled():
            try:
                result = safe_post_tweet(tweet_text)
                mark_posted(item_id, result)
                posted.append(result)
                _log_entry(f"X post {i + 1} posted", result.get("url", "unknown"))
            except Exception as e:
                mark_failed(item_id, str(e))
                _log_entry(f"X post {i + 1} FAILED", str(e))
        else:
            mark_skipped(item_id, "AUTO_POST_X not enabled or auto_post_x=False")

    # X thread
    if x_thread:
        thread_content = json.dumps(x_thread)
        item_id = add_to_queue(
            post_urn=post_urn,
            platform="x_thread",
            content=thread_content,
            requires_review=False,
            metadata={"tweet_count": len(x_thread)},
        )
        queued_thread.append(item_id)

        if auto_post_x and is_auto_post_enabled():
            try:
                results = safe_post_thread(x_thread)
                mark_posted(item_id, {"thread": results})
                posted.extend(results)
                _log_entry("X thread posted", f"{len(results)} tweets — {results[0].get('url', '')}")
            except Exception as e:
                mark_failed(item_id, str(e))
                _log_entry("X thread FAILED", str(e))
        else:
            mark_skipped(item_id, "AUTO_POST_X not enabled")

    return {
        "queued_single": queued_single,
        "queued_thread": queued_thread,
        "posted": posted,
    }


def _queue_beehiiv(post_urn: str, beehiiv_version: Optional[dict], pillar: int) -> Optional[str]:
    """
    Create a Beehiiv draft from the generated newsletter version.
    Always creates as draft — never sends.

    Returns:
        beehiiv_post_id or None
    """
    if not beehiiv_version:
        return None

    subject = beehiiv_version.get("subject_line", "Untitled Newsletter")
    preview_text = beehiiv_version.get("preview_text", "")
    body_md = beehiiv_version.get("body_markdown", "")
    cta_text = beehiiv_version.get("cta_button_text", "Learn More")
    cta_url = beehiiv_version.get("cta_url", "https://mottoappraisal.carrd.co")

    # Add CTA button to body HTML
    body_html = markdown_to_html(body_md)
    cta_html = (
        f'\n\n<div style="text-align:center;margin:32px 0;">'
        f'<a href="{cta_url}" style="background:#1a1a2e;color:#fff;padding:12px 28px;'
        f'border-radius:4px;text-decoration:none;font-weight:600;">{cta_text}</a></div>'
    )
    full_html = body_html + cta_html

    # Queue as a draft record first
    item_id = add_to_queue(
        post_urn=post_urn,
        platform="beehiiv",
        content=json.dumps({"subject": subject, "preview_text": preview_text}),
        requires_review=True,
        metadata={"pillar": pillar, "status": "draft"},
    )

    try:
        result = create_post(
            subject=subject,
            preview_text=preview_text,
            body_html=full_html,
            status="draft",
        )
        mark_posted(item_id, result)
        _log_entry("Beehiiv draft created", f"ID: {result.get('id', 'unknown')} — {subject}")
        return result.get("id")
    except Exception as e:
        mark_failed(item_id, str(e))
        _log_entry("Beehiiv draft FAILED", str(e))
        return None


def _queue_review_item(
    post_urn: str,
    platform: str,
    content: dict | str,
    pillar: int,
    section_name: str,
) -> str:
    """Add a manual review item to the queue. Returns item_id."""
    if isinstance(content, dict):
        content_str = json.dumps(content, indent=2)
    else:
        content_str = str(content)

    item_id = add_to_queue(
        post_urn=post_urn,
        platform=platform,
        content=content_str,
        requires_review=True,
        metadata={"section": section_name, "pillar": pillar},
    )
    _log_entry(f"Manual review queued: {section_name}", f"Item ID: {item_id}")
    return item_id


