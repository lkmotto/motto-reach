def distribute(post_data: dict, auto_post_x: bool = False) -> dict:
    """
    Full distribution pipeline for one piece of content.

    Args:
        post_data: {
            post_urn: str,      — unique identifier for the LinkedIn post
            post_text: str,     — full text of the post
            pillar: int,        — content pillar (1–6)
            format_type: str,   — "educational" | "data" | "opinion" | "promo"
            char_count: int,    — character count (optional, computed if absent)
            published_at: str,  — ISO8601 timestamp of original post
        }
        auto_post_x: bool — if True AND AUTO_POST_X env var is "true", post to X immediately.
                            Both conditions must be met. Defaults to False.

    Returns:
        Summary dict of what was queued, posted, and flagged for review.
    """
    post_urn: str = post_data.get("post_urn", f"urn:li:activity:{int(time.time())}")
    post_text: str = post_data.get("post_text", "")
    pillar: int = int(post_data.get("pillar", 1))

    if not post_text:
        raise ValueError("post_data must include 'post_text'.")

    _init_log_run(post_urn, pillar)

    summary: dict = {
        "post_urn": post_urn,
        "pillar": pillar,
        "run_at": _now(),
        "classification": {},
        "transform_cost": {},
        "queued": {
            "x_posts": [],
            "x_thread": [],
            "beehiiv_draft_id": None,
            "review_items": [],
            "community": {
                "reddit": 0,
                "linkedin_groups": 0,
                "facebook_groups": 0,
            },
        },
        "posted": [],
        "company_page": None,
        "errors": [],
        "results_file": None,
    }

    # ------------------------------------------------------------------
    # Step 1: Classify
    # ------------------------------------------------------------------
    _log_section("Step 1: Classification")
    try:
        content_class = classify(post_data)
        summary["classification"] = content_class
        _log_entry("Promotion risk", content_class["promotion_risk_level"])
        _log_entry("Public feed safe", str(content_class["public_feed_safe"]))
        _log_entry("Subreddit safe", str(content_class["subreddit_safe"]))
        _log_entry("Long form safe", str(content_class["long_form_safe"]))
        _log_entry("Recommended platforms", ", ".join(content_class["recommended_platforms"]))
    except Exception as e:
        error_msg = f"Classification failed: {e}"
        summary["errors"].append(error_msg)
        _log(f"❌ {error_msg}")
        return summary

    # ------------------------------------------------------------------
    # Step 2: Transform
    # ------------------------------------------------------------------
    _log_section("Step 2: Transform (Claude generation)")
    try:
        transform_output = transform(post_data, content_class, pillar)
        cost_info = transform_output.get("cost_tracking", {})
        summary["transform_cost"] = {
            "total_estimated_usd": cost_info.get("total_estimated_cost_usd", 0),
            "total_input_tokens": cost_info.get("total_input_tokens", 0),
            "total_output_tokens": cost_info.get("total_output_tokens", 0),
            "model": cost_info.get("model", "unknown"),
        }
        _log_entry(
            "Transform cost",
            f"${cost_info.get('total_estimated_cost_usd', 0):.4f} "
            f"({cost_info.get('total_input_tokens', 0)} input / "
            f"{cost_info.get('total_output_tokens', 0)} output tokens)",
        )
    except Exception as e:
        error_msg = f"Transform failed: {e}\n{traceback.format_exc()}"
        summary["errors"].append(error_msg)
        _log(f"❌ {error_msg}")
        return summary

    # ------------------------------------------------------------------
    # Step 3: Queue X posts
    # ------------------------------------------------------------------
    _log_section("Step 3: X (Twitter) Distribution")
    x_posts: list[str] = transform_output.get("X_POSTS") or []
    x_thread: list[str] = transform_output.get("X_THREAD") or []

    if x_posts or x_thread:
        try:
            x_result = _queue_x_posts(
                post_urn, x_posts, x_thread, auto_post_x, transform_output
            )
            summary["queued"]["x_posts"] = x_result["queued_single"]
            summary["queued"]["x_thread"] = x_result["queued_thread"]
            summary["posted"].extend(x_result["posted"])
            _log_entry("X posts queued", str(len(x_result["queued_single"])))
            _log_entry("X thread queued", "Yes" if x_result["queued_thread"] else "No")
            _log_entry("X posts auto-posted", str(len(x_result["posted"])))
        except Exception as e:
            error_msg = f"X queueing failed: {e}"
            summary["errors"].append(error_msg)
            _log(f"❌ {error_msg}")
    else:
        _log("_No X content generated (public_feed_safe=False)_")

    # ------------------------------------------------------------------
    # Step 4: Queue Beehiiv draft
    # ------------------------------------------------------------------
    _log_section("Step 4: Beehiiv Newsletter Draft")
    beehiiv_version = transform_output.get("BEEHIIV_VERSION")
    if beehiiv_version:
        try:
            draft_id = _queue_beehiiv(post_urn, beehiiv_version, pillar)
            summary["queued"]["beehiiv_draft_id"] = draft_id
            if draft_id:
                _log_entry("Beehiiv draft created", draft_id)
            else:
                _log("_Beehiiv draft creation failed — check errors_")
        except Exception as e:
            error_msg = f"Beehiiv failed: {e}"
            summary["errors"].append(error_msg)
            _log(f"❌ {error_msg}")
    else:
        _log("_No Beehiiv content generated (long_form_safe=False and outreach_snippet_safe=False)_")

    # ------------------------------------------------------------------
    # Step 5: Manual review queue (Reddit, Facebook, LinkedIn groups)
    # ------------------------------------------------------------------
    _log_section("Step 5: Manual Review Queue")

    review_items: list[str] = []

    # LinkedIn Group
    if transform_output.get("LINKEDIN_GROUP_VERSION"):
        item_id = _queue_review_item(
            post_urn, "linkedin_group",
            transform_output["LINKEDIN_GROUP_VERSION"],
            pillar, "LINKEDIN_GROUP_VERSION"
        )
        review_items.append(item_id)

    # Facebook Group
    if transform_output.get("FACEBOOK_GROUP_VERSION"):
        item_id = _queue_review_item(
            post_urn, "facebook_group",
            transform_output["FACEBOOK_GROUP_VERSION"],
            pillar, "FACEBOOK_GROUP_VERSION"
        )
        review_items.append(item_id)

    # Reddit Post
    if transform_output.get("REDDIT_POST_VERSION"):
        item_id = _queue_review_item(
            post_urn, "reddit",
            transform_output["REDDIT_POST_VERSION"],
            pillar, "REDDIT_POST_VERSION"
        )
        review_items.append(item_id)

    # Reddit Comment
    if transform_output.get("REDDIT_COMMENT_VERSION"):
        item_id = _queue_review_item(
            post_urn, "reddit",
            transform_output["REDDIT_COMMENT_VERSION"],
            pillar, "REDDIT_COMMENT_VERSION"
        )
        review_items.append(item_id)

    # Medium / Substack / LinkedIn Article (long form — always manual)
    for section, platform in [
        ("MEDIUM_VERSION", "medium"),
        ("SUBSTACK_VERSION", "substack"),
        ("LINKEDIN_ARTICLE_VERSION", "linkedin"),
    ]:
        if transform_output.get(section):
            item_id = _queue_review_item(
                post_urn, platform,
                transform_output[section],
                pillar, section
            )
            review_items.append(item_id)

    summary["queued"]["review_items"] = review_items
    _log_entry("Manual review items queued", str(len(review_items)))

    # ------------------------------------------------------------------
    # Step 6: Company page post
    # ------------------------------------------------------------------
    _log_section("Step 6: LinkedIn Company Page Post")
    try:
        company_result = _post_to_company_page(post_text, pillar)
        summary["company_page"] = company_result
        if company_result and company_result.get("success"):
            summary["posted"].append(company_result)
    except Exception as e:
        error_msg = f"Company page step failed: {e}"
        summary["errors"].append(error_msg)
        _log(f"❌ {error_msg}")

    # ------------------------------------------------------------------
    # Step 7: Queue community content (Reddit, LinkedIn groups, FB groups)
    # ------------------------------------------------------------------
    _log_section("Step 7: Community Content Queuing")
    try:
        community_counts = _queue_community_content(post_data, transform_output, content_class)
        summary["queued"]["community"] = community_counts
        _log_entry(
            "Community totals",
            f"Reddit: {community_counts['reddit']} | "
            f"LI Groups: {community_counts['linkedin_groups']} | "
            f"FB Groups: {community_counts['facebook_groups']}",
        )
    except Exception as e:
        error_msg = f"Community content queueing failed: {e}\n{traceback.format_exc()}"
        summary["errors"].append(error_msg)
        _log(f"❌ {error_msg}")

    # ------------------------------------------------------------------
    # Step 8: Save full results
    # ------------------------------------------------------------------
    _log_section("Step 8: Results Persistence")

    full_results = {
        "post_data": post_data,
        "summary": summary,
        "transform_output": transform_output,
    }

    try:
        results_path = _save_results(post_urn, full_results)
        summary["results_file"] = str(results_path)
        _log_entry("Results saved", str(results_path))
    except Exception as e:
        _log(f"⚠️  Failed to save results file: {e}")

    # Queue stats
    try:
        stats = get_stats()
        _log_entry("Queue total", str(stats["status_counts"]["total"]))
        _log_entry("Pending", str(stats["status_counts"]["pending"]))
        _log_entry("Review required", str(stats["status_counts"]["review_required"]))
    except Exception:
        pass

    # Final summary log
    community = summary["queued"]["community"]
    _log("\n---")
    _log(f"**Run complete**: {_now()}")
    _log(
        f"**Cost**: ${summary['transform_cost'].get('total_estimated_usd', 0):.4f} | "
        f"**Errors**: {len(summary['errors'])} | "
        f"**Review items**: {len(review_items)} | "
        f"**Community queued**: Reddit={community['reddit']}, "
        f"LI Groups={community['linkedin_groups']}, "
        f"FB Groups={community['facebook_groups']}"
    )
    _log("\n---\n")

    return summary


