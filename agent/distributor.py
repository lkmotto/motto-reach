# ---------------------------------------------------------------------------
# Logging helpers
# ---------------------------------------------------------------------------

def _now() -> str:
    return time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())


def _log(message: str) -> None:
    """Append a timestamped log line to distribution_log.md."""
    LOG_PATH.parent.mkdir(parents=True, exist_ok=True)
    with LOG_PATH.open("a") as f:
        f.write(f"{message}\n")


def _log_section(title: str) -> None:
    _log(f"\n### {title}")


def _log_entry(label: str, value: str) -> None:
    _log(f"- **{label}**: {value}")


def _init_log_run(post_urn: str, pillar: int) -> None:
    """Write run header to log."""
    LOG_PATH.parent.mkdir(parents=True, exist_ok=True)
    if not LOG_PATH.exists():
        with LOG_PATH.open("w") as f:
            f.write("# Motto Appraisal — Content Distribution Log\n\n")
            f.write("_Auto-generated by distributor.py_\n\n")
            f.write("---\n\n")

    _log(f"\n## Run: {_now()}")
    _log(f"**Post URN**: `{post_urn}`  ")
    _log(f"**Pillar**: {pillar}  ")
    _log("")


