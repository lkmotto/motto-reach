def _post_to_company_page(
    post_text: str,
    pillar: int,
    image_path: Optional[str] = None,
) -> Optional[dict]:
    """
    Post a rephrased version to the company page.
    Called automatically after personal LinkedIn post.
    Skipped if POST_TO_COMPANY_PAGE env var is not "true".

    Returns the result dict from post_to_company_page(), or None if skipped/failed.
    """
    if os.environ.get("POST_TO_COMPANY_PAGE", "false").lower() != "true":
        _log("_Company page post skipped (POST_TO_COMPANY_PAGE != true)_")
        return None

    try:
        _log_entry("Company page", "Rephrasing for brand voice...")
        brand_text = rephrase_for_brand(post_text, pillar)

        if not brand_text:
            _log_entry("Company page SKIPPED", "rephrase_for_brand returned empty string")
            return None

        result = post_to_company_page(
            text=brand_text,
            image_urn=None,  # image upload handled separately if needed
        )

        if result.get("success"):
            _log_entry(
                "Company page posted",
                f"URN: {result.get('post_urn', 'unknown')} — "
                f"{result.get('company_page_url', '')}",
            )
        else:
            _log_entry(
                "Company page FAILED",
                f"Status {result.get('status_code', '?')}: {result.get('error', 'unknown error')[:100]}",
            )

        return result

    except Exception as e:
        error_msg = f"Company page post exception: {e}"
        log_str = traceback.format_exc()
        _log(f"❌ {error_msg}")
        return {"success": False, "error": error_msg, "traceback": log_str}


