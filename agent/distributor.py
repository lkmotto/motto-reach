def _queue_community_content(
    post_data: dict,
    transform_output: dict,
    content_class: dict,
) -> dict:
    """
    Add Reddit, LinkedIn group, and Facebook group content to their respective queues.
    Returns counts of items queued per platform.

    Reddit and LinkedIn groups are ALWAYS manual review — never auto-post.
    Facebook managed groups: only auto-post if in confirmed-managed list (currently none).
    """
    post_urn: str = post_data.get("post_urn", "")
    post_text: str = post_data.get("post_text", "")
    pillar: int = int(post_data.get("pillar", 1))

    counts = {
        "reddit": 0,
        "linkedin_groups": 0,
        "facebook_groups": 0,
    }

    # ---- Reddit ----
    _log_section("Step 7a: Reddit Community Queue")
    try:
        reddit_version = transform_output.get("REDDIT_POST_VERSION")
        if content_class.get("subreddit_safe", False) or reddit_version:
            title = ""
            if isinstance(reddit_version, dict):
                title = reddit_version.get("title", "")
            if not title:
                # Derive a short title from the first line of the post
                first_line = post_text.split("\n")[0].strip()
                title = first_line[:280] if first_line else "DFW Appraisal Insight"

            item_ids = queue_for_subreddits(
                post_text=post_text,
                title=title,
                source_post_urn=post_urn,
                pillar=pillar,
                reddit_version=reddit_version if isinstance(reddit_version, dict) else None,
            )
            counts["reddit"] = len(item_ids)
            _log_entry("Reddit items queued", str(counts["reddit"]))
        else:
            _log("_Reddit skipped — subreddit_safe=False and no REDDIT_POST_VERSION_")
    except Exception as e:
        _log(f"❌ Reddit queueing failed: {e}")

    # ---- LinkedIn Groups ----
    _log_section("Step 7b: LinkedIn Groups Queue")
    try:
        li_group_version = transform_output.get("LINKEDIN_GROUP_VERSION")
        group_safe_text = ""
        discussion_question = None

        if isinstance(li_group_version, dict):
            group_safe_text = li_group_version.get("body", li_group_version.get("text", ""))
            discussion_question = li_group_version.get("discussion_question")
        elif isinstance(li_group_version, str):
            group_safe_text = li_group_version

        if group_safe_text or li_group_version:
            item_ids = queue_for_groups(
                post_text=post_text,
                group_safe_version=group_safe_text or post_text,
                source_post_urn=post_urn,
                pillar=pillar,
                discussion_question=discussion_question,
            )
            counts["linkedin_groups"] = len(item_ids)
            _log_entry("LinkedIn group items queued", str(counts["linkedin_groups"]))
        else:
            _log("_LinkedIn groups skipped — no LINKEDIN_GROUP_VERSION generated_")
    except Exception as e:
        _log(f"❌ LinkedIn groups queueing failed: {e}")

    # ---- Facebook Groups ----
    _log_section("Step 7c: Facebook Groups Queue")
    try:
        fb_version = transform_output.get("FACEBOOK_GROUP_VERSION")
        fb_text = ""

        if isinstance(fb_version, dict):
            fb_text = fb_version.get("body", fb_version.get("text", ""))
        elif isinstance(fb_version, str):
            fb_text = fb_version

        final_fb_text = fb_text or post_text

        # Build a list of relevant group entries and queue each
        from tools.facebook_groups import RELEVANT_GROUP_TYPES, queue_for_manual_review as _fb_queue

        fb_item_ids: list[str] = []
        for group_name in RELEVANT_GROUP_TYPES:
            encoded_name = group_name.replace(" ", "%20")
            group_url = f"https://www.facebook.com/search/groups/?q={encoded_name}"
            item_id = _fb_queue(
                group_name=group_name,
                group_url=group_url,
                post_text=final_fb_text,
                source_post_urn=post_urn,
                pillar=pillar,
            )
            if item_id:
                fb_item_ids.append(item_id)

        counts["facebook_groups"] = len(fb_item_ids)
        _log_entry("Facebook group items queued", str(counts["facebook_groups"]))

    except Exception as e:
        _log(f"❌ Facebook groups queueing failed: {e}")

    return counts


