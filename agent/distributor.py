"""
distributor.py — Main Distribution Orchestrator
Motto Appraisal Service | Content Distribution Pipeline

Full distribution pipeline for one piece of LinkedIn content:
  1. Classify
  2. Transform to all platform variants (via Claude)
  3. Queue X posts for auto-posting (if AUTO_POST_X=true)
  4. Queue Beehiiv draft (always draft, never auto-sent)
  5. Add Reddit/Facebook group versions to manual review queue
  6. Post rephrased version to company page (if POST_TO_COMPANY_PAGE=true)
  7. Queue community content (Reddit subreddits, LinkedIn groups, Facebook groups)
  8. Log everything to /home/user/workspace/cron_tracking/distribution_log.md

Usage:
    from agent.distributor import distribute

    result = distribute(post_data, auto_post_x=False)
"""

